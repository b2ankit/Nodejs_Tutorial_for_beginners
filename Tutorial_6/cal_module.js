/*===================================== Defining function of calculator as a Module : Start============================== */
module.exports = {
    addition : function(a,b)
    {
        return(a+b);
    },

    subtraction : function(a,b)
    {
        return(b-a);
    },

    multiplication : function(a,b)
    {
        return(a*b);
    },

    divison : function(a,b)
    {
        return(a/b);
    },

    mod : function(a,b)
    {
        return(b%a);
    },
   
}
/*===================================== Defining function of calculator as a Module : END============================== */